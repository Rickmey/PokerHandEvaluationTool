        /// <summary>
        /// Fast Bitcounting method (adapted from snippets.org)
        /// </summary>
        /// <param name="bitField">ulong to count</param>
        /// <returns>number of set bits in ulong</returns>
        public static int BitCount(ulong bitField)
        {
            return
                bits[(int)(bitField & 0xFFUL)] +
                bits[(int)((bitField & 0xFF00UL) >> 8)] +
                bits[(int)((bitField & 0xFF0000UL) >> 16)] +
                bits[(int)((bitField & 0xFF000000UL) >> 24)] +
                bits[(int)((bitField & 0xFF00000000UL) >> 32)] +
                bits[(int)((bitField & 0xFF0000000000UL) >> 40)] +
                bits[(int)((bitField & 0xFF000000000000UL) >> 48)] +
                bits[(int)((bitField & 0xFF00000000000000UL) >> 56)];
        }
